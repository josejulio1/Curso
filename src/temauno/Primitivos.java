package temauno;

public class Primitivos {
    public static void main(String[] args) {
        // En Java, existen los tipos de datos primitivos y los referenciados

        // Los tipos de datos primitivos almacenan únicamente datos simples como un número
        int a = 4;

        // Los tipos de datos referenciados son aquellos que su tipo de dato es una clase.
        // Para saber si es una clase, todas las clases en Java comienzan con mayúscula
        String texto = "Hola Mundo";
    }
}